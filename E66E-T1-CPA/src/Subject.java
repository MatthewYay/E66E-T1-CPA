public class Subject {

	//XIAO QUAN
	private String name;
	private String code;
	private String type;
	
	public Subject(String name, String code, String type)
	{
		this.name = name;
		this.code = code;
		this.type = type;
		
	}
	public String getName() 
	{
		return name;
	}
	public String getCode() 
	{
		return code;
	}
	public String getType()
	{
		return type;
	}
}
